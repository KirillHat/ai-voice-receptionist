"""Menu lookup over data/menu.json.

The JSON is generated by scripts/scrape_menu.py from the Novikov website.
We expose three primitives the FAQ matcher needs:

- has_item(query)     -> Optional[Item]   "do you have X?"
- list_category(name) -> list[Item]       "what desserts do you have?"
- categories()        -> list[str]        useful for follow-up suggestions
"""

from __future__ import annotations

import json
import re
import unicodedata
from dataclasses import dataclass
from functools import lru_cache
from pathlib import Path

DATA_PATH = Path(__file__).resolve().parents[2] / "data" / "menu.json"


@dataclass(frozen=True)
class MenuItem:
    slug: str
    name: str
    url: str
    categories: tuple[str, ...]
    price: str | None = None
    description: str | None = None
    dietary: tuple[str, ...] = ()
    allergens: tuple[str, ...] = ()


@dataclass(frozen=True)
class MenuIndex:
    items: tuple[MenuItem, ...]
    by_slug: dict[str, MenuItem]
    by_norm_name: dict[str, MenuItem]
    by_category_norm: dict[str, tuple[MenuItem, ...]]

    @property
    def category_names(self) -> list[str]:
        return sorted({c for it in self.items for c in it.categories})


